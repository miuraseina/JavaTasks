package javaCodingTaskB2;



public class Prefecture {


	private int id;
	private String name;
	private int capitalCityId;

	public Prefecture(int id, String name, int capitalCityId) {
		this.id = id;
		this.name = name;
		this.capitalCityId = capitalCityId;
	}

	public int getId() {
		return id;
	}

	public String getName() {
		return name;
	}

	public int getCapitalCityId() {
		return capitalCityId;
	}




}



	
